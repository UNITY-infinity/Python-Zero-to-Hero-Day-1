#Takes two inputs,
#stores in variable x and y, 
#and prints the result of x to the power y

#input x and y
x = int(input("Enter a value as base: "))
y = int(input("Enter a value as power to which the base will be raised: "))

#print x to the power of y
print(x,' to the power of ',y,' is equal to: ',x**y)